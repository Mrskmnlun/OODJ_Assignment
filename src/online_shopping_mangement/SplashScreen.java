    public static void main(String args[]) {
        SplashScreen sp = new SplashScreen();
        sp.setVisible(true);
        
        try{
            for(int i=0;i<=100;i++){
                Thread.sleep(10);
                sp.LoadingValue.setText(i+"%");
                
                if(i==10){
                    sp.LoadingLabel.setText("Turning On Modules....");
                }
                if(i==20){
                    sp.LoadingLabel.setText("Loading Modules....");
                }
                if(i==50){
                    sp.LoadingLabel.setText("Connecting to Database....");
                }
                if(i==70){
                    sp.LoadingLabel.setText("Connect Successful....");
                }
                if(i==80){
                    sp.LoadingLabel.setText("Launching System....");
                }
                if(i==100){
                    sp.dispose();
                    Choosemain cm = new Choosemain();
                    cm.setVisible(true);
                }
                sp.LoadingBar.setValue(i);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
